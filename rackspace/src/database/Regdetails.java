package database;

public class Regdetails {
 Integer cnum;
 String  cname;
 String  gender;
 String  city;
 String  hobbiess;
public  Regdetails() {
	super();
	this.cnum = cnum;
	this.cname = cname;
	this.gender = gender;
	this.city = city;
	this.hobbiess = hobbiess;
}
public Integer getCnum() {
	return cnum;
}
public void setCnum(Integer cnum) {
	this.cnum = cnum;
}
public String getCname() {
	return cname;
}
public void setCname(String cname) {
	this.cname = cname;
}
public String getGender() {
	return gender;
}
public void setGender(String gender) {
	this.gender = gender;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public String getHobbiess() {
	return hobbiess;
}
public void setHobbiess(String hobbiess) {
	this.hobbiess = hobbiess;
}
	

}
